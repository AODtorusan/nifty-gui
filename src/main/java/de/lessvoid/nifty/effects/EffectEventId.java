package de.lessvoid.nifty.effects;

/**
 * possible effect event id's that you can attach effects too.
 * @author void
 */
public enum EffectEventId {

  /**
   * on start of screen.
   */
  onStartScreen,

  /**
   * on end of screen.
   */
  onEndScreen,

  /**
   * on focus.
   */
  onFocus,

  /**
   * on click.
   */
  onClick,

  /**
   * on hover.
   */
  onHover,

  /**
   * on active.
   */
  onActive
}
