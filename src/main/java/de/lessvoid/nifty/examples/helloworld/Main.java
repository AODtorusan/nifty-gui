    /** Desired window width. */
    private static final int WINDOW_WIDTH = 1024;

    /** Desired window height. */
    private static final int WINDOW_HEIGHT = 768;


    public static void main(String[] args) {
        new Main().run();
    }

    public Main() {
        try {
            init(false);
        } catch (LWJGLException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
    }

    /**
     * Game's main loop.
     */
    public void run() {
        boolean finished = false;
        while (!finished) {
            Display.update();

            // Check for close requests
            if (Display.isCloseRequested()) {
                finished = true;
            } else if (Display.isActive()) {
                render();
            } else {
                // Only bother rendering if the window is visible or dirty
                if (Display.isVisible() || Display.isDirty()) {
                    render();
                }
            }
        }
    }


    public static float angle = 0;
    
    private void render() {
        GL11.glClear(GL11.GL_COLOR_BUFFER_BIT | GL11.GL_DEPTH_BUFFER_BIT);
        
        angle += 0.001;
        GL11.glPushMatrix();

        GL11.glScalef(0.1f, 0.1f, 0.1f);
        GL11.glRotatef((float) (angle * 180 / Math.PI), 1, 1, 1);
        GL11.glColor3f(1, 1, 1);

        /*
        GL11.glBegin(GL11.GL_TRIANGLES);
        int i;
        for (i = 0; i < triangles.length; i++)
        {
            int vi = 3 * triangles[i];
            GL11.glVertex3f((float) vertices[vi], (float) vertices[vi + 1], (float) vertices[vi + 2]);
        }

        GL11.glEnd();
        */
        
        GL11.glEnableClientState(GL11.GL_VERTEX_ARRAY);
        FloatBuffer verticesBuffer = ByteBuffer.allocateDirect(vertices.length * 4).asFloatBuffer().put(vertices);
        verticesBuffer.flip();
        
        GL11.glVertexPointer(3, 0, verticesBuffer);
        
        IntBuffer trianglesBuffer = ByteBuffer.allocateDirect(triangles.length * 4).asIntBuffer().put(triangles);
        trianglesBuffer.flip();
        GL11.glDrawElements(GL11.GL_TRIANGLES, trianglesBuffer);
        
        GL11.glPopMatrix();
    }


    private void init(boolean fullscreen) throws LWJGLException {
        Display.setTitle("ola");
        Display.setFullscreen(fullscreen);

        Display.setDisplayMode(findDesiredDisplayMode());
        Display.create();

        // Enable depth testing
        GL11.glEnable(GL11.GL_DEPTH_TEST);       

        GL11.glMatrixMode(GL11.GL_PROJECTION);
        GL11.glLoadIdentity();

        GLU.gluPerspective(90f, 800 / 600.0f, 0.1f, 1000.0f);
        GL11.glViewport(0, 0, 800, 600);

        GL11.glMatrixMode(GL11.GL_MODELVIEW);
        GL11.glLoadIdentity();

        GLU.gluLookAt(0f, 0f, 10f,
                0f, 0f, 0f,
                0f, 1f, 0);
    }


    private DisplayMode findDesiredDisplayMode() throws LWJGLException {
        // Find desired display mode
        DisplayMode chosenMode = null;
        DisplayMode[] modes = Display.getAvailableDisplayModes();
        for (DisplayMode mode : modes) {
            if (mode.getWidth() == WINDOW_WIDTH
                    && mode.getHeight() == WINDOW_HEIGHT) {
                chosenMode = mode;
                break;
            }
        }

        if (chosenMode == null) {
            System.err.println("Unable to find the appropriate display mode.");
            System.exit(1);
        }

        return chosenMode;
    }

